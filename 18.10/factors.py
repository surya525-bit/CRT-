import math
n=int(input())
a=int(math.sqrt(n))
l=[]
k=0
for i in range(1,a+1):
    if(n%i==0 and i not in l):
        l.append(i)
        if(n//i not in l):
            l.append(n//i)
l=sorted(l)
for i in l:
    if(i==n):
        l.remove(i)
for i in l:
    k=i+k
if(k==n):
    print("perfect")
if(k>n):
    print("abundant")
if(k<n):
    print("deficient")
        















































































































































































































































































































































































































































































































































































































